# In Python, functions are blocks of reusable code that perform a 
# specific task, defined using the def keyword. Built-in functions 
# are provided by Python (e.g., print(), len()), while user-defined 
# functions are created by the programmer.


# Example :

def greet(name):
    return f"Hello, {name}!"

print(greet("Moiz"))  # Output: Hello, Alice!



# Note:
# Here, greet is a user-defined function, and print is a built-in function.






