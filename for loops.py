
# A for loop in Python is used to iterate over a sequence (like a list or string) and 
# execute a block of code for each element.


# iterable objects
# 1. list
# 2. String 
# 3. dictionaries


fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)


# output
# apple
# banana
# cherry



# range()
# range( start, stop, step)

for i in range(5):
    print(i)

# output 
# 0
# 1
# 2
# 3
# 4